class Registration {
	int regno;
	int identificationnumber;
	Registration(int regno, int identificationnumber) {
		this.regno = regno;
		this.identificationnumber = identificationnumber;
	}
	int getregno() {
		return this.regno;
	}
	int getidentificationnumber() {
		return this.identificationnumber;
	}
}
